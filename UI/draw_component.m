                   comp.aerodynamics.relative_velocity(2)*ones(size(Y)), ...
                   comp.aerodynamics.relative_velocity(3)*ones(size(Z)), "AutoScale","on","LineWidth", 0.2, "Color",ColorMap(80,:))


