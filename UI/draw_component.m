patch(ax, new_mesh, 'FaceColor',       ColorMap(70,:), ...
                    'EdgeColor',       'none',        ...
                    'FaceLighting',    'gouraud',     ...
                    'AmbientStrength', 0.1, ...
                    'FaceAlpha',       0.7);
%l = light(ax, "Style","local","comp.rigid_body.position", comp.rigid_body.position' + [1 0 0]);
l = light(ax);
l.Color = [1 1 1];
lighting(ax, "flat")
axis(ax, "tight")

end


